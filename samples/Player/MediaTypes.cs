        private static void AddMappings(string mimeType, params string[] extensions)
        {
            if (_mediaTypeToExtensions.TryGetValue(mimeType, out var mimeTypeExtensions))
            {
                foreach (var extension in extensions)
                {
                    mimeTypeExtensions.Add(extension);
                }
            }
            else
            {
                _mediaTypeToExtensions.Add(mimeType, new SortedSet<string>(extensions));
            }

            foreach (var extension in extensions)
            {
                if (_extensionToMimeTypes.TryGetValue(extension, out var mimeTypes))
                {
                    mimeTypes.Add(mimeType);
                }
                else
                {
                    _extensionToMimeTypes.Add(extension, new SortedSet<string> { mimeType });
                }
            }
        }

        private static void ValidateMappings()
        {
            var missingMappings = new Dictionary<string, SortedSet<string>>();

            foreach (var format in AVInputFormat.All)
            {
                foreach (var extension in format.Extensions)
                {
                    var mediaTypes = GetMediaTypes(extension);
                    if (!mediaTypes.Any())
                    {
                        if (missingMappings.TryGetValue(extension, out var suggestedMimeTypes))
                        {
                            foreach (var suggestedMimeType in format.MimeTypes)
                            {
                                suggestedMimeTypes.Add(suggestedMimeType);
                            }
                        }
                        else
                        {
                            missingMappings.Add(extension, new SortedSet<string>(format.MimeTypes));
                        }
                    }
                }
            }

            foreach (var format in AVOutputFormat.All)
            {
                foreach (var extension in format.Extensions)
                {
                    var mediaTypes = GetMediaTypes(extension);
                    if (!mediaTypes.Any())
                    {
                        if (missingMappings.TryGetValue(extension, out var suggestedMimeTypes))
                        {
                            foreach (var suggestedMimeType in format.MimeTypes)
                            {
                                suggestedMimeTypes.Add(suggestedMimeType);
                            }
                        }
                        else
                        {
                            missingMappings.Add(extension, new SortedSet<string>(format.MimeTypes));
                        }
                    }
                }
            }

            foreach (var missingMapping in missingMappings)
            {
                Debug.Print($"No media types found for extension '{missingMapping.Key}'. Suggested media type(s): {string.Join(", ", missingMapping.Value)}");
            }
        }

        public static IEnumerable<string> GetExtensions(string mimeType)
        {
            if (mimeType == null)
            {
                throw new ArgumentNullException(nameof(mimeType));
            }

            if (_mediaTypeToExtensions.TryGetValue(mimeType, out var extension))
            {
                return extension;
            }

            return Enumerable.Empty<string>();
        }

        public static IEnumerable<string> GetMediaTypes(string extension)
        {
            if (extension == null)
            {
                throw new ArgumentNullException(nameof(extension));
            }

            if (_extensionToMimeTypes.TryGetValue(extension, out var mimeTypes))
            {
                return mimeTypes;
            }
            return Enumerable.Empty<string>();
        }

        public static bool IsVideoMediaType(string extension)
        {
            var mediaTypes = GetMediaTypes(extension);
            var isAudioMediaType = mediaTypes.Any(mimeType => mimeType.StartsWith("video/"));
            return isAudioMediaType;
        }

        public static bool IsAudioMediaType(string extension)
        {
            var mediaTypes = GetMediaTypes(extension);
            var isAudioMediaType = mediaTypes.Any(mimeType => mimeType.StartsWith("audio/"));
            return isAudioMediaType;
        }
